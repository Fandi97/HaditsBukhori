package id.aplikasi.skripsi.afandi1147050008.model;

public class Hadist {
    private String no;
    private String kitab;
    private String arab;
    private String terjemahan;
    private String time;
    private String bruteTime;
    private String boyerTime;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getKitab() {
        return kitab;
    }

    public void setKitab(String kitab) {
        this.kitab = kitab;
    }

    public String getArab() {
        return arab;
    }

    public void setArab(String arab) {
        this.arab = arab;
    }

    public String getTerjemahan() {
        return terjemahan;
    }

    public void setTerjemahan(String terjemahan) {
        this.terjemahan = terjemahan;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getBruteTime() {
        return bruteTime;
    }

    public void setBruteTime(String bruteTime) {
        this.bruteTime = bruteTime;
    }

    public String getBoyerTime() {
        return boyerTime;
    }

    public void setBoyerTime(String boyerTime) {
        this.boyerTime = boyerTime;
    }
}
